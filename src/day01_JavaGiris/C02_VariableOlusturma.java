package day01_JavaGiris;

public class C02_VariableOlusturma {

    public static void main(String[] args) {
        int sayi = 10 ;

        sayi = 2 * sayi;
        System.out.println( sayi );
        sayi = 5 + sayi;
        System.out.println( sayi );
        sayi = 5 * sayi - 7;
        System.out.println( sayi );
        sayi = 40;
        System.out.println( sayi );




    }
}
